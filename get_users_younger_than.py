def get_users_younger_than(data:dict, age:int)->list:
    """Gets all users younger than a certain age from the data
    Args:
        data (dict): The data from the JSON file
        age (int): The age to filter users by
    Returns:
        list: A list of users
    """
    l = []
    for i in data['users']:
        if i['age']< age:
            l.append(i)
